/**
 * @mainpage
 *
 * Utility and Devices
 * ---
 *
 * This is a library of data structures and hardware interface drivers
 * implemented using the CObject library. 
 *
 * Documentation Guide
 * ---
 * 
 * There are two main tabs to use:
 *
 * * Modules
 * * Data Structures
 *
 * In the **Modules** tab, data structures are orginized by their application. 
 * For example, all code which implements a queue is collected in the module
 * **Queues**. 
 *
 * In the **Data Structures/Data Structures** tab is a list of structures by
 * name. 
 *
 * The description of each structure includes:
 *
 * * An inheritance diagram (minimized by default)
 * * All public member functions which operate on the structure
 * * A description of what the structure is used for
 * * In a small block labelled **Attention** there is a description
 * on how to call inherited functions.
 * * Initialization functions / constructors
 * * A description of each public method
 */
